<li>Постановка и решение краевых задач для тел различной конфигурации и структуры при механических, электромагнитных, радиационных, тепловых и прочих воздействиях, в том числе применительно к объектам новой техники.</li>
<li>Математические модели и численные методы анализа применительно к задачам, не допускающим прямого аналитического исследования.</li>
<li>Экспериментальные методы исследования процессов деформирования, повреждения и разрушения материалов, в том числе объектов, испытывающих фазовые структурные превращения при внешних воздействиях.</li>
