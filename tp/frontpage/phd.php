<li>Теория моделей деформируемых тел с простой и сложной структурой.</li>
<li>Мезомеханика многоуровневых сред со структурой.</li>
<li>Механика композиционных и интеллектуальных материалов и конструкций.</li>
<li>Теория упругости, пластичности и ползучести.</li>
